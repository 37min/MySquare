	int revCode; //후기번호
	String commContents; //코멘트 내용
	String commId;
	Date commDate; //코멘트작성일시
	
	
	public String getCommId() {
		//System.out.println("NOW IN COMM.DTO.commId:"+this.commId);
		
		return commId;
	}
	public void setCommId(String commId) {

		//System.out.println("NOW IN COMM.DTO.commId:"+commId);
		this.commId = commId;
	}

	
	public int getCommCode() {
		//System.out.println("NOW IN COMM.DTO.getCommCode:"+this.commCode);
		return commCode;
	}

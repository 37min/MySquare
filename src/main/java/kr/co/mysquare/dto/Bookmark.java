package kr.co.mysquare.dto;

public class Bookmark 
{
	int bmCode; //즐겨찾기 번호
	String locName; //명소이름
	String id; //사용자 아이디
	
	public int getBmCode() {
		return bmCode;
	}
	public void setBmCode(int bmCode) {
		this.bmCode = bmCode;
	}
	public String getLocName() {
		return locName;
	}
	public void setLocName(String locName) {
		this.locName = locName;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}

}//end of Bookmark class

